start:
    ; Initialise DS to the program's data segment
    mov  ax, @data
    mov  ds, ax

;---------------------------------------------------------
; Main_Menu
;   – Displays the splash screen BMP.
;   – Resets scores each time we land here.
;   – Dispatches: L → Learn, T → Test, I → Instructions.
;   – Esc here is the ONLY way to exit to DOS.
;---------------------------------------------------------
Main_Menu:
    mov  [word score_ok],    0
    mov  [word score_total], 0

    ; Enter VGA mode 13h (320×200, 256 colours)
    mov  ax, 13h
    int  10h

    ; ShowBMP(FileName, Header, Palette, ScrLine, filehandle)
    push offset file_start
    push offset Header
    push offset Palette
    push offset ScrLine
    push offset filehandle
    call ShowBMP                ; cleans 10 bytes itself (ret 10)

@@Main_Menu_Wait:
    mov  ah, 00h
    int  16h                    ; AH = scan code, AL = ASCII

    cmp  ah, 01h                ; scan code 01h = Esc
    jne  @@MM_NotEsc            ; trampoline: Esc at main menu → exit to DOS
    jmp  exit_game_final
@@MM_NotEsc:

    and  al, 0DFh               ; force uppercase
    cmp  al, 'L'
    jne  @@MM_NotL
    jmp  set_learn
@@MM_NotL:
    cmp  al, 'T'
    jne  @@MM_NotT
    jmp  set_test
@@MM_NotT:
    cmp  al, 'I'
    jne  @@Main_Menu_Wait
    jmp  Instructions_Screen

;---------------------------------------------------------
; Instructions_Screen
;   – Displays the instructions BMP.
;   – Esc returns to Main_Menu (circular navigation).
;---------------------------------------------------------
Instructions_Screen:
    mov  ax, 13h
    int  10h

    push offset file_instr
    push offset Header
    push offset Palette
    push offset ScrLine
    push offset filehandle
    call ShowBMP

@@Instr_Wait:
    mov  ah, 00h
    int  16h
    cmp  ah, 01h                ; Esc → back to main menu
    jne  @@Instr_Wait           ; trampoline
    jmp  Main_Menu

;---------------------------------------------------------
; Mode selection — sets game_mode and enters the round loop
;---------------------------------------------------------
set_learn:
    mov  [byte game_mode], 0
    jmp  next_round

set_test:
    mov  [byte game_mode], 1
    jmp  next_round

;---------------------------------------------------------
; next_round
;   Clears the screen, picks a random letter, branches to
;   the active game mode.
;---------------------------------------------------------
next_round:
    call clear_screen
   push [tick_seed]
    call get_random
    pop  [tick_seed]
    mov  [current_idx], ax

    cmp  [byte game_mode], 1
    jne  @@go_learn             ; trampoline for je test_mode
    jmp  test_mode
@@go_learn:

;---------------------------------------------------------
; LEARN MODE
;   Shows the full letter+Morse tile, prompts for Morse
;   input and validates it.  Esc returns to Main_Menu.
;---------------------------------------------------------
learn_mode:
    ; draw_tile(letter_index, morse_only=0) → full tile
    push [word current_idx]
    push 0
    call draw_tile              ; cleans 4 bytes

    ; Copy the 5-char Morse pattern into target_ptrn
    ; ES must equal DS here — ShowBMP leaves ES=0A000h (VGA) so we fix it
    mov  ax, [current_idx]
    mov  bx, 5
    mul  bx                     ; AX = index into patterns table
    mov  si, offset patterns
    add  si, ax
    push ds
    pop  es                     ; ES = DS so movsb targets data segment
    mov  di, offset target_ptrn
    mov  cx, 5
    rep  movsb

    ; Cache the target letter character
    mov  bx, [current_idx]
    mov  al, [letters + bx]
    mov  [target_char], al

    ; Reset input buffer index
    mov  [word input_idx], 0

    ; Position text cursor at top-left and print prompt
    mov  ah, 02h
    xor  bh, bh
    xor  dx, dx
    int  10h

    ; Print "Letter: X  Enter Morse ..."
    mov  si, offset msg_prompt
    mov  ah, 0Eh
    mov  bl, 0Fh
@@learn_PrintPrompt:
    lodsb
    cmp  al, '$'
    je   @@learn_PrintLetter
    int  10h
    jmp  @@learn_PrintPrompt

@@learn_PrintLetter:
    mov  ah, 0Eh
    mov  al, [target_char]
    int  10h

    mov  si, offset msg_instr
@@learn_PrintInstr:
    lodsb
    cmp  al, '$'
    je   @@learn_InputLoop
    int  10h
    jmp  @@learn_PrintInstr

;---------------------------------------------------------
; Morse input loop (shared by Learn mode)
;   j → dot  (stored as 'J')
;   k → dash (stored as 'K')
;   Enter → validate
;   Esc   → Main_Menu
;---------------------------------------------------------
@@learn_InputLoop:
    mov  ah, 07h
    int  21h                    ; read character (no echo)

    cmp  al, 1Bh                ; Esc (ASCII 27)
    jne  @@learn_NotEsc         ; trampoline
    jmp  Main_Menu
@@learn_NotEsc:

    cmp  al, 0Dh                ; Enter (ASCII 13)
    je   @@learn_Finish

    cmp  al, 'j'
    je   @@learn_Dot
    cmp  al, 'k'
    je   @@learn_Dash
    jmp  @@learn_InputLoop

@@learn_Dot:
    cmp  [word input_idx], 4    ; maximum 4 symbols per pattern
    ja   @@learn_InputLoop
    mov  bx, [input_idx]
    mov  [byte user_input + bx], 'J'
    inc  [word input_idx]
    mov  ah, 0Eh
    mov  al, '.'
    mov  bl, 0Fh
    int  10h
    push 1                      ; short beep duration
    call make_sound             ; cleans 2 bytes
    jmp  @@learn_InputLoop

@@learn_Dash:
    cmp  [word input_idx], 4
    ja   @@learn_InputLoop
    mov  bx, [input_idx]
    mov  [byte user_input + bx], 'K'
    inc  [word input_idx]
    mov  ah, 0Eh
    mov  al, '-'
    mov  bl, 0Fh
    int  10h
    push 4                      ; long beep duration
    call make_sound             ; cleans 2 bytes
    jmp  @@learn_InputLoop

@@learn_Finish:
    ; Terminate user_input with '$'
    mov  bx, [input_idx]
    mov  [byte user_input + bx], '$'

    ; check_answer(target_ptrn, user_input)
    push offset user_input
    push offset target_ptrn
    call check_answer           ; cleans 4 bytes, result in AL

    cmp  al, 1
    jne  @@learn_Wrong

    call flash_green
    jmp  next_round

@@learn_Wrong:
    call flash_red
    jmp  next_round

;---------------------------------------------------------
; TEST MODE
;   Shows only the Morse rows (no letter glyph).
;   Player presses the letter key they think it represents.
;   Esc returns to Main_Menu.
;---------------------------------------------------------
test_mode:
    inc  [word score_total]

    ; draw_tile(letter_index, morse_only=1) → Morse rows only
    push [word current_idx]
    push 1
    call draw_tile              ; cleans 4 bytes

    call draw_score

    ; Wait for a single keypress
    mov  ah, 00h
    int  16h                    ; AH = scan code, AL = ASCII

    cmp  ah, 01h                ; Esc → back to main menu
    jne  @@test_NotEsc          ; trampoline
    jmp  Main_Menu
@@test_NotEsc:

    and  al, 0DFh               ; force uppercase
    sub  al, 'A'                ; convert ASCII letter → 0-based index
    cmp  al, [byte current_idx] ; compare with expected index
    jne  @@test_Wrong

    ; Correct answer
    inc  [word score_ok]
    push [word current_idx]
    push 0
    call draw_tile              ; reveal full tile
    call flash_green
    jmp  next_round

@@test_Wrong:
    call flash_red
    ; Clear screen to black so tile is visible, then reveal full tile
    call clear_screen
    push [word current_idx]
    push 0
    call draw_tile
    ; Wait for any keypress so player can study the correct answer
    mov  ah, 00h
    int  16h
    cmp  ah, 01h                ; Esc → back to main menu
    jne  @@test_wrong_continue
    jmp  Main_Menu
@@test_wrong_continue:
    jmp  next_round

;---------------------------------------------------------
; exit_game_final
;   Restore text mode (INT 10h / AX=03h) and terminate
;   the process (INT 21h / AX=4C00h).
;---------------------------------------------------------
exit_game_final:
    mov  ax, 03h
    int  10h                    ; restore 80×25 text mode
    mov  ax, 4C00h
    int  21h                    ; exit to DOS with code 0

