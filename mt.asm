;=========================================================
; PROC: ShowBMP
;
; Loads a BMP from disk and blits it to VGA mode 13h.
; Also programs the VGA DAC palette from the BMP palette.
;
; Calling convention — caller pushes left-to-right, NEAR call:
;
;   push offset FileName      [bp+12]  near ptr  (DS-relative)
;   push offset Header        [bp+10]  near ptr
;   push offset Palette       [bp+8]   near ptr
;   push offset ScrLine       [bp+6]   near ptr
;   push offset filehandle    [bp+4]   near ptr
;   call ShowBMP
;
; Stack layout after prologue (NEAR call, MODEL SMALL):
;   [bp+0]  = saved BP
;   [bp+2]  = near return address (IP)
;   [bp+4]  = filehandle ptr      (last pushed  = closest to ret addr)
;   [bp+6]  = ScrLine ptr
;   [bp+8]  = Palette ptr
;   [bp+10] = Header ptr
;   [bp+12] = FileName ptr        (first pushed = furthest from ret addr)
;
; Callee cleans: ret 10  (5 word params × 2 bytes)
;=========================================================
proc ShowBMP
    push bp
    mov  bp, sp

    push ax
    push bx
    push cx
    push dx
    push si

    ; ── 1. Open the file (INT 21h / AH=3Dh, read-only) ──
    mov  ah, 3Dh
    xor  al, al                 ; access mode: read-only
    mov  dx, [bp+12]            ; DS:DX -> filename
    int  21h
    jnc  @@ShowBMP_Opened       ; trampoline: no carry = success, continue
    jmp  @@ShowBMP_Error        ; carry set = file open failed
@@ShowBMP_Opened:

    ; Store handle via the caller-supplied pointer
    mov  bx, [bp+4]             ; bx = address of filehandle word
    mov  [bx], ax               ; save returned handle
    mov  bx, ax                 ; bx = file handle for subsequent calls

    ; ── 2. Read 54-byte BMP header (discard – used for seek) ──
    mov  ah, 3Fh
    mov  cx, 54
    mov  dx, [bp+10]            ; DS:DX -> Header buffer
    int  21h

    ; ── 3. Read 256-colour palette (1 024 bytes, 4 bytes/entry) ──
    mov  ah, 3Fh
    mov  cx, 400h               ; 1 024 bytes
    mov  dx, [bp+8]             ; DS:DX -> Palette buffer
    int  21h

    ; ── 4. Program VGA DAC (port 3C8h/3C9h) ──
    ;    BMP palette entries: [B, G, R, 0] each 8-bit (0-255).
    ;    VGA DAC expects 6-bit values (0-63) → divide by 4 (shr 2).
    mov  si, [bp+8]             ; SI -> first palette entry
    mov  cx, 256
    mov  dx, 3C8h
    xor  al, al
    out  dx, al                 ; start at palette index 0
    inc  dx                     ; dx = 3C9h  (data port)
@@ShowBMP_PalLoop:
    mov  al, [si+2]             ; Red component
    shr  al, 2
    out  dx, al
    mov  al, [si+1]             ; Green component
    shr  al, 2
    out  dx, al
    mov  al, [si]               ; Blue component
    shr  al, 2
    out  dx, al
    add  si, 4                  ; advance to next palette entry
    loop @@ShowBMP_PalLoop

    ; ── 5. Blit rows bottom-up (BMP storage order) ──
    ;    VGA framebuffer base = 0A000h
    ;    Row n from top  →  offset = n × 320
    ;    CX counts rows from 200 down to 1 (bottom row first).
    mov  ax, 0A000h
    mov  es, ax
    mov  cx, 200

@@ShowBMP_RowLoop:
    push cx

    ; Compute destination offset in VGA memory:
    ;   di = (cx-1) * 320  =  (cx-1)*256 + (cx-1)*64
    mov  di, cx
    dec  di
    mov  ax, di
    shl  di, 8                  ; di  = (cx-1) * 256
    shl  ax, 6                  ; ax  = (cx-1) * 64
    add  di, ax                 ; di  = (cx-1) * 320

    ; Read one screen-width row from file into ScrLine
    mov  ah, 3Fh
    mov  cx, 320
    mov  dx, [bp+6]             ; DS:DX -> ScrLine buffer
    int  21h

    ; Copy ScrLine → VGA framebuffer
    mov  si, [bp+6]
    mov  cx, 320
    cld
    rep  movsb

    pop  cx
    loop @@ShowBMP_RowLoop

    ; ── 6. Close file ──
    mov  ah, 3Eh                ; AH=3Eh  Close file
    ; BX still holds the file handle from step 1
    int  21h

@@ShowBMP_Error:
    pop  si
    pop  dx
    pop  cx
    pop  bx
    pop  ax
    pop  bp
    ret  10                     ; callee cleans 5 × 2 = 10 bytes
endp ShowBMP

;=========================================================
; PROC: check_answer
;
; Compares the user's typed Morse code against the target
; pattern and returns a result flag in AL.
;
; Stack layout after prologue:
;   [bp+4]  near ptr  target_ptrn  (pattern to match)
;   [bp+6]  near ptr  user_input   (what the user typed)
;
; Returns:
;   AL = 1  if input matches pattern exactly
;   AL = 0  otherwise
;
; Callee cleans: ret 4
;=========================================================
proc check_answer
    push bp
    mov  bp, sp

    push si
    push di
    push bx

    mov  si, [bp+6]             ; SI -> user_input
    mov  di, [bp+4]             ; DI -> target_ptrn

@@check_answer_Loop:
    mov  al, [si]
    mov  bl, [di]
    cmp  bl, '$'                ; reached end-of-pattern sentinel?
    je   @@check_answer_PatEnd
    cmp  al, bl                 ; characters must match
    jne  @@check_answer_Wrong
    inc  si
    inc  di
    jmp  @@check_answer_Loop

@@check_answer_PatEnd:
    ; Pattern exhausted – user input must also be at '$'
    cmp  al, '$'
    je   @@check_answer_Correct

@@check_answer_Wrong:
    mov  al, 0
    jmp  @@check_answer_Done

@@check_answer_Correct:
    mov  al, 1

@@check_answer_Done:
    pop  bx
    pop  di
    pop  si
    pop  bp
    ret  4                      ; callee cleans 2 × 2 = 4 bytes
endp check_answer

;=========================================================
; PROC: make_sound
;
; Generates a brief tone via the PC speaker (PIT channel 2)
; then silences it after a caller-specified delay.
;
; Stack layout after prologue:
;   [bp+4]  word  duration (passed directly to INT 15h CX)
;
; Callee cleans: ret 2
;=========================================================
proc make_sound
    push bp
    mov  bp, sp

    push ax
    push cx
    push dx

    ; Program PIT channel 2: mode 3 (square wave), ~2 500 Hz
    mov  al, 0B6h
    out  43h, al                ; PIT command register
    mov  ax, 2500               ; reload value ≈ 2 500 Hz
    out  42h, al                ; low byte
    mov  al, ah
    out  42h, al                ; high byte

    ; Enable speaker gate (bits 0-1 of port 61h)
    in   al, 61h
    or   al, 03h
    out  61h, al

    ; Delay using BIOS INT 15h / AH=86h (microsecond wait)
    ;   CX:DX = duration in microseconds
    mov  cx, [bp+4]             ; high word of delay count
    mov  dx, 0
    mov  ah, 86h
    int  15h

    ; Disable speaker gate
    in   al, 61h
    and  al, 0FCh
    out  61h, al

    pop  dx
    pop  cx
    pop  ax
    pop  bp
    ret  2                      ; callee cleans 1 × 2 = 2 bytes
endp make_sound

;=========================================================
; PROC: draw_tile
;
; Renders one 20×20 letter tile (or its 20×10 Morse-only
; lower half) from letters_pixels into VGA mode 13h.
;
; Stack layout after prologue:
;   [bp+4]  word  morse_only   0 = draw full 20-row tile
;                              1 = draw Morse rows only (rows 10-19)
;   [bp+6]  word  letter_index 0-25
;
; Callee cleans: ret 4
;=========================================================
proc draw_tile
    push bp
    mov  bp, sp

    push si
    push di
    push es

    ; ── Compute source pointer into letters_pixels ──
    ;    Each entry is 400 bytes (20 rows × 20 cols).
    mov  ax, [bp+6]
    xor  dx, dx
    mov  bx, 400
    mul  bx                     ; AX = letter_index × 400
    mov  si, offset letters_pixels
    add  si, ax                 ; SI -> start of this letter's data

    ; ── Point ES at VGA framebuffer ──
    mov  ax, 0A000h
    mov  es, ax

    ; ── Branch: full tile vs Morse-only rows ──
    cmp  [word bp+4], 1
    je   @@draw_tile_MorseOnly

    ; Full tile: draw all 20 rows, destination centred on screen
    mov  di, (80*320) + 150     ; top-left pixel of tile area
    mov  cx, 20                 ; 20 rows to draw
    jmp  @@draw_tile_Blit

@@draw_tile_MorseOnly:
    ; Skip the first 200 bytes (rows 0-9, the letter glyph)
    add  si, 200
    ; Draw only rows 10-19 (Morse indicator), shifted down
    mov  di, (90*320) + 150
    mov  cx, 10                 ; 10 rows to draw

@@draw_tile_Blit:
    ; Inner loop: copy 20 bytes per row, then advance DI by
    ; (320 - 20) = 300 to reach the start of the next row.
    push cx
    mov  cx, 20
    rep  movsb                  ; copy 20 bytes from DS:SI to ES:DI
    add  di, 320-20             ; skip to start of next screen row
    pop  cx
    loop @@draw_tile_Blit

    pop  es
    pop  di
    pop  si
    pop  bp
    ret  4                      ; callee cleans 2 × 2 = 4 bytes
endp draw_tile


proc get_random
    push bp
    mov  bp, sp
    push bx
    push cx
    push dx

    ; Get both words of tick counter
    mov  ah, 00h
    int  1Ah                ; CX = high word, DX = low word

    ; Mix CX and DX together with seed from stack
    xor  dx, cx             ; combine both tick words
    xor  dx, [bp+4]         ; mix with seed passed on stack

    ; LCG: seed = seed * 25173 + 13849
    mov  ax, dx
    mov  bx, 25173
    mul  bx                 ; DX:AX = ax * 25173
    add  ax, 13849
    mov  [bp+4], ax         ; save updated seed back to stack

    ; Get result in range 0-25
    xor  dx, dx
    mov  bx, 26
    div  bx                 ; DX = AX mod 26
    mov  ax, dx             ; return in AX

    pop  dx
    pop  cx
    pop  bx
    pop  bp
    ret                     ; caller cleans — reads updated seed back with pop
endp get_random
;=========================================================
; PROC: clear_screen
;
; Blanks the entire 320×200 VGA framebuffer to colour 0.
; No parameters.
;=========================================================
proc clear_screen
    push es
    push di
    push cx
    push ax

    mov  ax, 0A000h
    mov  es, ax
    xor  di, di
    mov  cx, 320*200/2          ; fill with word writes
    xor  ax, ax
    rep  stosw

    pop  ax
    pop  cx
    pop  di
    pop  es
    ret
endp clear_screen

;=========================================================
; PROC: flash_green
;
; Floods the screen with colour 0Ah (bright green) then
; waits ~500 ms via INT 15h before returning.
; No parameters.
;=========================================================
proc flash_green
    push es
    push di
    push cx
    push ax
    push dx

    mov  ax, 0A000h
    mov  es, ax
    xor  di, di
    mov  cx, 320*200/2
    mov  ax, 0A0Ah              ; byte 0A0Ah = two bright-green pixels
    rep  stosw

    ; Wait ~500 ms (INT 15h / AH=86h, CX:DX in microseconds)
    mov  ah, 86h
    mov  cx, 0007h
    mov  dx, 0A120h             ; 0x0007A120 = 500 000 µs
    int  15h

    pop  dx
    pop  ax
    pop  cx
    pop  di
    pop  es
    ret
endp flash_green

