;=========================================================
; PROC: draw_score
;
; Prints "Score: X/Y" at the top-left of the text cursor
; using BIOS TTY output (INT 10h / AH=0Eh).
;
; Reads globals: score_ok, score_total, score_str.
; No parameters.
;=========================================================
proc draw_score
    push ax
    push bx
    push cx
    push dx
    push si

    ; Move text cursor to row 0, column 0
    mov  ah, 02h
    xor  bh, bh
    xor  dx, dx
    int  10h

    ; Print "Score: " label
    mov  si, offset score_str
    mov  bl, 0Ah                ; bright green attribute (teletype)
@@draw_score_Label:
    lodsb
    cmp  al, '$'
    je   @@draw_score_OkNum
    mov  ah, 0Eh
    int  10h
    jmp  @@draw_score_Label

@@draw_score_OkNum:
    ; Print score_ok (supports 0-99)
    mov  ax, [score_ok]
    xor  dx, dx
    mov  bx, 10
    div  bx                     ; AX = tens digit, DX = units digit
    cmp  ax, 0
    je   @@draw_score_OkUnits   ; suppress leading zero
    add  al, '0'
    mov  ah, 0Eh
    int  10h
@@draw_score_OkUnits:
    mov  al, dl
    add  al, '0'
    mov  ah, 0Eh
    int  10h

    ; Print '/' separator
    mov  ah, 0Eh
    mov  al, '/'
    int  10h

    ; Print score_total (supports 0-99)
    mov  ax, [score_total]
    xor  dx, dx
    mov  bx, 10
    div  bx
    cmp  ax, 0
    je   @@draw_score_TotUnits
    add  al, '0'
    mov  ah, 0Eh
    int  10h
@@draw_score_TotUnits:
    mov  al, dl
    add  al, '0'
    mov  ah, 0Eh
    int  10h

    pop  si
    pop  dx
    pop  cx
    pop  bx
    pop  ax
    ret
endp draw_score

